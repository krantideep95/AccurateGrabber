    public void enableAll(boolean b) {
        txtUsername.setEnabled(b);
        txtName.setEnabled(b);
        txtPassword.setEnabled(b);

        txtContact.setEnabled(b);
        txtAddress.setEnabled(b);
        txtEmailid.setEnabled(b);

    }

    public void afterSave() {
        al = mus.getAllUsers();
        if (addupdateflag == 1) {
            count = al.size() - 1;
            showRecord();
        }
        if (addupdateflag == -1) {
            showRecord();
        }
        btnAdd.setEnabled(true);
        btnUpdate.setEnabled(true);
    }

    private void showRecord() {
        UserBean objbean = (UserBean) al.get(count);
        lblUserid.setText(String.valueOf(objbean.getUserid()));
        txtUsername.setText(objbean.getUsername());
        txtPassword.setText(objbean.getPassword());
        txtName.setText(objbean.getName());
        txtContact.setText(objbean.getContactno());
        txtAddress.setText(objbean.getAddress());
        txtEmailid.setText(objbean.getEmail());
        btnFirst.setEnabled(true);
        btnLast.setEnabled(true);
        btnNext.setEnabled(true);
        btnPrevious.setEnabled(true);
        if (count == 0) {
            btnFirst.setEnabled(false);
            btnPrevious.setEnabled(false);

        }
        if (count == al.size() - 1) {
            btnLast.setEnabled(false);
            btnNext.setEnabled(false);
        }
        enableAll(false);
        btnSave.setEnabled(false);

    }

