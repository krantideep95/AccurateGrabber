    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        btnFirst.setEnabled(false);
        btnLast.setEnabled(false);
        btnNext.setEnabled(false);
        btnPrevious.setEnabled(false);
        btnUpdate.setEnabled(false);
        btnAdd.setEnabled(false);
        if (txtUsername.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter username", "Username Field", JOptionPane.ERROR_MESSAGE);
            txtUsername.requestFocus();
            return;
        } else if (txtPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter password", "password Field", JOptionPane.ERROR_MESSAGE);
            txtPassword.requestFocus();
            return;
        } else if (txtName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter name", "name Field", JOptionPane.ERROR_MESSAGE);
            txtName.requestFocus();
            return;
        }
        if (txtContact.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter Contact", "Contact Field", JOptionPane.ERROR_MESSAGE);
            txtContact.requestFocus();
            return;
        } else if (txtAddress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter Address", "Username Field", JOptionPane.ERROR_MESSAGE);
            txtAddress.requestFocus();
            return;
        } else if (txtEmailid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter EmailID", "Email ID Field", JOptionPane.ERROR_MESSAGE);
            txtEmailid.requestFocus();
            return;
        }

        char c[] = txtContact.getText().toCharArray();
        if (c.length <= 10) {
            for (int i = 0; i < c.length; i++) {
                int ascii = (int) c[i];
                if (ascii >= 48 && ascii <= 57) {

                } else {
                    JOptionPane.showMessageDialog(this, "Contact no. not correct", "Contact no.", JOptionPane.ERROR_MESSAGE);
                    txtContact.requestFocus();

                    return;
                }

            }
        } else {
            JOptionPane.showMessageDialog(this, "Contact no. not correct", "Contact no.", JOptionPane.ERROR_MESSAGE);
            txtContact.requestFocus();
            return;
        }
        int userid = Integer.parseInt(lblUserid.getText());
        String username = txtUsername.getText();
        String name = txtName.getText();

        UserBean objbean = new UserBean();
        objbean.setName(name);
        objbean.setUsername(username);
        objbean.setUserid(userid);

        objbean.setPassword(txtPassword.getText());
        objbean.setContactno(txtContact.getText());
        objbean.setAddress(txtAddress.getText());
        objbean.setEmail(txtEmailid.getText());

        if (addupdateflag == 1) {
            if (mus.addUser(objbean) == true) {
                JOptionPane.showMessageDialog(this, "record inserted", "record inserted successfully", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "record not inserted", "record insertion failed", JOptionPane.INFORMATION_MESSAGE);
            }
        } else if (addupdateflag == -1) {
            if (mus.updateUser(objbean) == true) {
                JOptionPane.showMessageDialog(this, "record updated", "record updated successfully", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "record not updated", "record updation failed", JOptionPane.INFORMATION_MESSAGE);
            }
        }

        afterSave();
    }//GEN-LAST:event_btnSaveActionPerformed

